    private void spremiFormu(FrmMod frmMod) {
       switch (frmMod) {
            case unos:
                  this.setBorder(BorderFactory.createTitledBorder(BorderFactory.createEtchedBorder(), "Unos novog leka u bazu lekova",TitledBorder.LEFT, TitledBorder.TOP));
                btnSacuvaj.setVisible(true);
                //btnIzmeni.setVisible(false);
                break;
                
            case izmena:
                this.setBorder(BorderFactory.createTitledBorder(BorderFactory.createEtchedBorder(), "Izmena leka",TitledBorder.LEFT, TitledBorder.TOP));
                popuniPolja(sesija.Sesija.getInstanca().getMapa().get("trenutniLek"));
                btnSacuvaj.setVisible(false);
                //btnIzmeni.setVisible(true);
                break;
                
            case pregled:
                this.setBorder(BorderFactory.createTitledBorder(BorderFactory.createEtchedBorder(), "Prikaz leka",TitledBorder.LEFT, TitledBorder.TOP));
                popuniPolja(sesija.Sesija.getInstanca().getMapa().get("trenutniLek"));
                btnSacuvaj.setVisible(false);
                txtNaziv.setEditable(false);
                txtKoncentracija.setEditable(false);
                txtGrupa.setEditable(false);
                txtNamena.setEditable(false);
                txtSastav.setEditable(false);
                txtKontraind.setEditable(false);
                break;
        }
    }


