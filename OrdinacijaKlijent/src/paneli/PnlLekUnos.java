    private boolean validacija(String naziv, String konc, String grupa, String sastav, String kontraind) {
        if (naziv.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Morate uneti naziv leka!");
            return false;
        }
        if (grupa.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Morate uneti grupu kojoj lek pripada!");
            return false;
        }
        if (sastav.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Morate uneti sastav leka!");
            return false;

        }
        if (kontraind.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Morate uneti kontraindikacije leka!");
            return false;

        }
       
        return true;
    }

    @Override
    public void popuniPolja(OpstaDomenskaKlasa odk) {
         Lek l = (Lek) odk;
        if (l != null) {
            txtNaziv.setText(l.getNaziv());
            txtKoncentracija.setText(l.getKoncentracija());
            txtGrupa.setText(l.getGrupaLekova());
            txtNamena.setText(l.getNamena());
            txtSastav.setText(l.getSastav());
            txtKontraind.setText(l.getKontraindikacije());


        }
    }

    
