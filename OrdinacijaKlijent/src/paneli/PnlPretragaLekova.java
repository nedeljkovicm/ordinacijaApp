    private void pripremiFormu() {
        try{
             model= new ModelTabeleLekovi();
             tblLekovi.setModel(model);
             List<Lek> lista=Kontroler.getInstanca().vratiSveLekove();
            model.setLista(lista);
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            Logger.getLogger(PnlKartonPretraga.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
 public void izaberi() {
        btnIzaberi.setVisible(true);
    }

    private void pripremiDialog(FrmMod frmMod) {
          if (frmMod != frmMod.unos) {
            int index = tblLekovi.getSelectedRow();

            if (index == -1) {

                JOptionPane.showMessageDialog(this, "Morate izabrati red!");
                return;
            }
            Lek l = model.getLista().get(index);
            sesija.Sesija.getInstanca().getMapa().put("trenutniLek", l);
        }
        
        PnlLekUnos panel = new PnlLekUnos(frmMod);
        FrmDialog dialog= new FrmDialog((Frame) SwingUtilities.getWindowAncestor(this), true, panel);
        dialog.setVisible(true);
    }

    @Override
    public void popuniPodatke() {
         if(sesija.Sesija.getInstanca().getMapa().get("trenutniLek")!=null){
        model.getLista().remove(sesija.Sesija.getInstanca().getMapa().get("trenutniLek"));
        model.ubaci((Lek) sesija.Sesija.getInstanca().getMapa().get("trenutniLek"));
        }
    }
    
    
