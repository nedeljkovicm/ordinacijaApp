    private void btnPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziActionPerformed
            
        if (txtNaziv.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Niste uneli kriterijum izbora");
            return;
        }
        
        String naziv = txtNaziv.getText();
        
        Lek l= new Lek();
        l.setNaziv(naziv);
        List<Lek> lista;
        model= new ModelTabeleLekovi();
        try {
            lista = kontroler.Kontroler.getInstanca().vratiLekove(l);
             if(lista.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Ne postoje rezultati pretrage");
                tblLekovi.setModel(model);
            }
            for (Lek lek : lista) {
                model.ubaci(lek);
            }
            tblLekovi.setModel(model);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            Logger.getLogger(PnlKartonPretraga.class.getName()).log(Level.SEVERE, null, ex);

        }
    }//GEN-LAST:event_btnPretraziActionPerformed

