    private void spremiTabelu() {
        try {
            model= new ModelTabelaKartoni();
            tblKartoni.setModel(model);
            List<Karton> lista=Kontroler.getInstanca().vratiSveKartone();
            model.setLista(lista);
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            Logger.getLogger(PnlKartonPretraga.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void pripremiDialog(FrmMod frmMod) {
        if (frmMod != frmMod.unos) {
            int index = tblKartoni.getSelectedRow();

            if (index == -1) {

                JOptionPane.showMessageDialog(this, "Morate izabrati red!");
                return;
            }
            Karton k = model.getLista().get(index);
            sesija.Sesija.getInstanca().getMapa().put("trenutniKarton", k);
        }
        
        PnlKartonUnosIzmena panel = new PnlKartonUnosIzmena(frmMod);
        FrmDialog dialog= new FrmDialog((Frame) SwingUtilities.getWindowAncestor(this), true, panel);
        dialog.setVisible(true);
    }

    @Override
    public void popuniPodatke() {
         if(sesija.Sesija.getInstanca().getMapa().get("trenutniKarton")!=null){
        model.getLista().remove(sesija.Sesija.getInstanca().getMapa().get("trenutniKarton"));
        model.ubaci((Karton) sesija.Sesija.getInstanca().getMapa().get("trenutniKarton"));
        }
    }
    
