    private void btnIzaberiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzaberiActionPerformed
         int index = tblKartoni.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Morate selektovati red u tabeli");
            return;
        }
        Karton k = model.getLista().get(index);
        sesija.Sesija.getInstanca().getMapa().put("trenutniKarton", k);
        (((FrmForma) SwingUtilities.getWindowAncestor((FrmForma) SwingUtilities.getWindowAncestor(this)))).getPanel().popuniPodatke();
       // ((FrmForma) SwingUtilities.getWindowAncestor(this)).dispose();
    }//GEN-LAST:event_btnIzaberiActionPerformed

    private void btnPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziActionPerformed
        
        if (txtPrezime.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Niste uneli kriterijum izbora");
            return;
        }
        
        String prezime = txtPrezime.getText();
        
        Karton kart= new Karton();
        kart.setPrezime(prezime);
        List<Karton> lista;
        model= new ModelTabelaKartoni();
        try {
            lista = kontroler.Kontroler.getInstanca().vratiKartone(kart);
             if(lista.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Ne postoje rezultati pretrage");
                tblKartoni.setModel(model);
            }
            for (Karton k : lista) {
                model.ubaci(k);
            }
            tblKartoni.setModel(model);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            Logger.getLogger(PnlKartonPretraga.class.getName()).log(Level.SEVERE, null, ex);

        }
    }//GEN-LAST:event_btnPretraziActionPerformed

