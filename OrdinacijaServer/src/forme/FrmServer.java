    public void postaviStatus(String poruka, boolean signal) {
       lblStatus.setText(poruka);
        btnPokreni.setEnabled(signal);
        btnZaustavi.setEnabled(!signal);
        pnlKorisnici.setVisible(!signal);
       pnlKonfiguracija.setVisible(signal);
    }

    public void odjaviKorisnika(Korisnik kor) {
        ModelTabeleKorisnici model = (ModelTabeleKorisnici) tblKorisnici.getModel();
        model.izbaciKorisnika(kor);
    }

    public void dodajPrijavljenogKorisnika(Korisnik k) {
         ModelTabeleKorisnici model = (ModelTabeleKorisnici) tblKorisnici.getModel();
        model.dodajKorisnika(k);
    }

    public void srediTabelu() {
        ModelTabeleKorisnici model = new ModelTabeleKorisnici();
        tblKorisnici.setModel(model);
    }

