    private void btnOdjaviActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOdjaviActionPerformed
         ModelTabeleKorisnici model = (ModelTabeleKorisnici) tblKorisnici.getModel();
        int index = tblKorisnici.getSelectedRow();
        if (index != -1) {
            Korisnik k = model.izbaciRed(index);
             try {
                 Kontroler.getInstanca().izbaciKorisnika(k);
             } catch (IOException ex) {
                 Logger.getLogger(FrmServer.class.getName()).log(Level.SEVERE, null, ex);
             }
        } else {
            JOptionPane.showMessageDialog(this, "Morate izabrati red tabele!");
        }
    }//GEN-LAST:event_btnOdjaviActionPerformed

